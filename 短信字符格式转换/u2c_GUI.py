class Example(QWidget):
	def __init__(self):
		super().__init__()
		self.initUI()

	def initUI(self):
		self.label1 = QLabel("待转换内容：（字符以空格或回车分隔）")
		self.label2 = QLabel("转换结果：")

		self.text1 = QTextEdit(EXAMPLE_1)
		self.text1.setAcceptRichText(False)
		self.text2 = QTextEdit(EXAMPLE_2)
		self.text2.setAcceptRichText(False)
		self.btn1 = QPushButton('转换')
		self.btn1.resize(10,10)

		grid = QGridLayout()
		grid.setSpacing(10)

		grid.addWidget(self.label1,1,0)
		grid.addWidget(self.label2,1,1)
		grid.addWidget(self.text1,2,0)
		grid.addWidget(self.text2,2,1)
		grid.addWidget(self.btn1,2,2)

		self.setLayout(grid)

		# 按钮连接到槽
		self.btn1.clicked.connect(self.buttonClicked)

		self.setGeometry(200,300,600,350)
		self.setWindowTitle('短信字符转换')
		self.show()

	def buttonClicked(self):
		string_origin = self.text1.toPlainText()
		string_result = self.u2c(string_origin)
		self.text2.setPlainText(''.join(string_result))
		# print(string_result)

	def u2c(self,data_str):
		result=[]
		if('\n' in data_str):
			data = data_str.split('\n')
		elif(' ' in data_str):
			data = data_str.split(' ')
		# print(data)
		for i in range(0,len(data)-1,2):
			try:
				a = int(data[i].strip().replace("0x",""),16)
				b = int(data[i+1].strip().replace("0x",""),16)
				res=b'\\u%02x%02x'%(a,b)
				res1=res.decode('unicode_escape')
				result.append(res1)
				# print(res1,end='')
			except Exception as e:
				print(e)

		return result

